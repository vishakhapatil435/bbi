public class Student
{
	private String name;
	private String rollNo;
	
	public  void setRollNo(String rollNo)
	{
		this.rollNo=rollNo;
	}
	public String getRollNo()
	{
		return rollNo;
	}
	public void setName(String n)
	{
		this.name=n;
	}
	public String getName()
	{
		return name;
	}
	
} 
